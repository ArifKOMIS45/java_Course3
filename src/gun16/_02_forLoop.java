package gun16;

public class _02_forLoop {
    public static void main(String[] args) {
        //while dongusu
        int i=0;
        while (i<5){
            System.out.println("w_i = " + i);
            i++; }
        //for dongusu
        for(int j =0; j<5; j++){
            System.out.println("f_j = " + j);
        }






    }
}
