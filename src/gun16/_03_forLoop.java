package gun16;

public class _03_forLoop {
    public static void main(String[] args) {
// o dan10dahil) sayilari ekrana yazdirin
        // 10 dan sifira kadar sifir dahil sayilari yazdirin
        for(int j =0; j<=10; j++){
            System.out.println("f_j = " + j);
        }

        for(int j =10; j>=0; j--){
            System.out.println("f_j = " + j);
        }




    }
}
