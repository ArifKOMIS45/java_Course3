public class _09_JavaaritmetikOperators {
    public static void main(String[] args) {

        int a=8;
        int b=4;
        System.out.println("toplama islemi = " +(a+ b));
        System.out.println("a+b = " +(a+b));
        System.out.println("5+2 = " +(5+2));

        System.out.println("Cikarma islemi");
        System.out.println("a-b = " +(a-b));
        System.out.println("5-2 = " +(5-2));

        System.out.println("Negatiflestirme");
        System.out.println("b'yi eksilersek = " + -b);

    }
}
