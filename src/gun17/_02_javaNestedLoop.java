package gun17;

public class _02_javaNestedLoop {
    public static void main(String[] args) {
// Aşağıdaki şekilde çıktı veren programı print içerisinde
// sadece bir tane # kullanarak yazıdırınız

//            #####
//            #####
//            #####
//            #####
//            #####


        for (int i = 1; i <= 5; i++)
        {
            for (int t = 1; t <= 5; t++)
            {
                System.out.print("#");
            }

            System.out.println();
        }


    }

}
